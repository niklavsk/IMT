<h4><strong><strong>Step One</strong>&nbsp;<strong>– Prep: Scheduled Meals and Basecamp</strong></strong></h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Before you bring your new cat home, there are some fundamental steps to accomplish that will give you a significant leg up on the process:</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>A.</strong>&nbsp;<strong>No free-feeding –</strong>&nbsp;Make sure you have switched your existing cat over to a routine of meal feeding, rather than free feeding her. This concept is foundational to my approach, and nowhere is it more important than during the introduction process. Once these scheduled meal times are established, it will set the stage for both your existing cat and your newcomer to experience a shared, ritualistic way of being: they get fed, around the same time, x-number of times per day. (More specifics about this shared meal time in a moment.)</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>B. Separate Base Camp</strong>&nbsp;<strong>(and the mandatory isolation phase)</strong>&nbsp;– Base camp is a defined area of your home that is the heart of a cat’s territory. So first, decide where you’re going to set up base camp for the new cat, so he feels like this space is essentially all his own. This could be the master or a spare bedroom, an office, or even the bathroom when there is no other option. As long as the human scent is strong, it will help the cat establish a sense of home by comingling scents.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>In addition, here are a few other important elements associated with successful base camp protocol:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><li><strong>Scent soakers:</strong>&nbsp;Because cats are all about scent, make sure that you have plenty of “scent soakers” in their base camp. Scent soakers are soft items that absorb a cat’s scent, and basically say “I live here,” and allow for rubbing, scratching, or lying in. Beds, blankets, carpets, cardboard scratchers, and scratching posts are all excellent scent soakers.</li><li><strong>No Peeking</strong>: One of the hallmarks of this integration method is that the new cat and the resident will not initially lay eyes on each other. This is non-negotiable. Ignore this part of the introduction process at your own peril!</li></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>Once your new cat demonstrates a notable comfort level in has base camp, it’s time for…</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><li><strong>Site swapping:</strong>&nbsp;This is where each cat gets to explore the other’s territory without ever laying eyes on each other. This is also an opportunity for key signposts—like cat trees, litter boxes, etc.—to take on a shared scent. This is crucial to the “getting to know you” process with cats, since so much of their communication is based on scent.</li></ul>
<!-- /wp:list -->

<!-- wp:heading {"level":4} -->
<h4>Follow this simple process for harmonious site swapping:</h4>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>1. Carry the newcomer out of his base camp, put him in the bathroom, and shut the door.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>2. Allow the resident cat to walk into the newcomer’s base camp, then shut that door.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>3. Allow the newcomer to explore the rest of the home.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>4. Rinse and repeat.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>And by the way, your new cat will let you know when he’s ready to move out of base camp and explore the other parts of the house. (It could be anywhere from a few hours to a few days.)</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><li><strong>The “Other Side of the Door” Feeding Ritual</strong>: This feeding ritual, which is all about creating a positive association between the newcomer and the resident cat, has evolved over the years, but by and large has always worked for me. What’s involved? Very simply, mealtime will consist of bowls set up on either side of a closed door. These bowls should start out far enough apart so the cats will walk up, eat, and walk away without incident, but close enough that they sense there’s another cat on the other side of the door. From there, we gradually move the bowls closer.</li></ul>
