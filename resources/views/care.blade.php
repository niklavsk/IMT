<!-- wp:heading {"level":5} -->
<h5>Give Them Shelter&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Feral cats need a space where they can escape the rain and extreme temperatures of winter and summer. You don't need to create anything too fancy — a simple plastic storage bin is more than enough to keep the kitties comfy. Cut a hole in the side of the bin and line it with something cozy like straw. In the winter, place the bin inside a larger one and add some foam board insulation between them.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":5} -->
<h5>Feed Them&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>One of the kindest things you can do is give outside kitties some yummy wet or dry food to keep their tummies full. But don't leave wet food out for long if they aren't interested in it, as it's more likely to grow bacteria that can make them sick.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Protect the food from inclement weather by placing it on a covered patio or&nbsp;set&nbsp;up a little tarp between two plastic-bin shelters and&nbsp;put&nbsp;the food there. Remember to clean the area daily to keep insects and other pests away.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":5} -->
<h5>Quench Their Thirst&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>A fresh bowl of water will help the stray felines stay hydrated. Place the bowl outside your kitty shelters to prevent them from becoming damp and yucky. If possible, look for a solar-powered heated water dish to keep the water from freezing in winter.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":5} -->
<h5>Contact a Rescue Group Practicing Trap-Neuter-Return&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Many nonprofit organizations&nbsp;provide trap-neuter-return, or TNR, services, or can refer you to a local group that does. This way, the strays will be spayed or neutered so they can't reproduce, reducing the&nbsp;amount&nbsp;of hungry mouths for you and your neighbors to feed. Once the procedure is finished, the kitties are safely returned to the neighborhood — plus they'll get a health check while at the vet.</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":5} -->
<h5>Determine if Cats are Actually Homeless&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>It's easy to assume a cat outside without a collar is a stray, but that furry feline might just be someone's pet. Check with your neighbors to determine if any cats in question have owners. If possible, ask owners to place collars on their furry friends so no one will think they are strays.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Some outdoor-owned kitties have microchips placed between their shoulder blades for identification. These can be read by a Universal Microchip Scanner. This way, a vet or shelter worker can find out the owner's contact information if the kitty is lost or mistaken for a stray.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:heading {"level":5} -->
<h5>Consider Finding a Home for a Stray&nbsp;</h5>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>If you find a lone cat or litter of kittens who are not feral, or adapted to the wild, check with local rescue organizations to see about finding the felines forever homes. Look for no-kill shelters and rescue organizations.&nbsp;You can also continue helping already sheltered cats by volunteering at local rescue shelters.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Note that if you befriend a cat who's happily feral, allow him to live within his outdoor colony, even if he seems to like being around you for a visit. Feral cats in colonies have their own social hierarchies and societies. If taken away from the colony, feral cats get anxious and often become chronically ill. Worse yet, feral cats often wind up scared, afraid and un-adoptable in animal shelters, whereas they have a better chance&nbsp;at&nbsp;a fulfilling life (albeit possibly shorter), if left in their colony.&nbsp;</p>
